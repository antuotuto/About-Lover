      <h3>企业级全球资金管理金融云</h3>
      <p>记者通过多家银行和企业了解后发现，“企业级全球资金管理金融云”项目还是具有明显优势和广阔前景的。无论是对于企业端或银行端，“金融云”平台都有不可或缺的意义。在传统应用中，如果企业要申请SWIFT技术支持，企业用户须自己动手安装和维护包括软、硬件在内的整套SWIFT系统，该项工作需耗用企业的服务器、网络以及维护人员等巨大的IT资源，而通过创新型的“金融云”平台模式，相当于为企业提供了一个完整的系统平台，在该架构下，企业只需成为SWIFT的交易银行业务联盟会员而不需要再专门安装和维护SWIFT系统，将大大降低了用户企业应用解决方案的费用和上线时间。 在“金融云”上的企业可以直接使用平台提供的“财资管理系统”对全球资金进行管理，而无需额外单独购买财资管理系统就可方便实现财务与资金管理集约化、一体化，建立合理的统筹模式，释放沉淀资金。</p>
      <h3>总结</h3>
      <p>由此可见，对参与的商业银行来说，通过“金融云”平台可以为其企业客户提供全面的全球资金管理服务，帮助企业汇集和整合其全球的资金，打破现阶段该领域几乎被国际大银行所掌握的局面。通过“金融云”平台，增加企业用户粘性、扩大用户群体，中资银行可进一步提高对企业全球运营服务的能力。与此同时，通过“金融云”共享平台，银行在服务企业的同时可更深入了解用户的交易需求，从而对业务合作的机会进行充分挖掘、利于开发更具个性化的产品及服务。</p>
