      <h3>全球运营</h3>
      <p>随着众多企业走出国门进行全球运营，银企关系变得越来越复杂。许多企业集团在设有众多海外机构的同时，也在全球不同银行开立了业务账户。为了获得银行账户直接和相关的信息，企业与银行通信时不得不采用“银企直连”、网上银行、手工柜台等操作方式。但遗憾的是，就当前银企对接方式而言，企业要实现高效率的资金全球运营和管理面临极大的挑战，最为突出的是，由于账户开立在全球不同银行，不同时区的各家银行连接方式、数据格式及发送时间完全不同，企业集团很难及时掌握整个集团全球资金的状况。甚至还有少数银行账户只能通过手工上报。因此很多企业只能通过Excel 表格人工汇总和计算，从而无法实现全球的及时汇总；再者，由于银行所采用的通信方式各不相同，常常难以与公司自身的财务管理系统或ERP系统建立并保持链接，加之成本相对高昂，这就很难实现支付发起和外汇确认流程的自动化；尤其是当人工流程繁多时，会在很大程度上限制业务的快速增长；还反映在，安全管理方面的复杂化；在风险控制与合规管理方面很难进行端对端的交易审计跟踪。</p>
      <h3>金融环境</h3>
      <p>近两年来，国内交易银行业务的认知和发展为企业资金管理创造了良好的金融环境。在针对海外账户实现“实时、可观、可控”的全球资金管理方面，无论是中资国际企业还是商业银行，都已经将跨境账户管理列入日程，认识到企业全球资金管理动态平衡、高效有序的重要性。财资易通“金融云”平台正是秉承着交易银行以现金管理为核心和“得账户者得天下”的战略理念，在开拓自主化研发应用的同时，积极寻求与国际银行金融电信组织SWIFT合作，为金融机构和企业搭建先进的标准化银行通信网关。企业用户通过“金融云”平台与 SWIFT 建立高效连接，可实现与其开户银行的业务通信。简言之，企业通过“主办行”直接利用平台提供的财资易通管理系统，由SWIFT渠道链接全球银行账户，从而实现企业级跨境资金交易和管理。其间使用SWIFT的加密网络和报文标准进行相关境外银行交易，可实现交易的电子化和自动化，并降低了操作风险。</p>
      <h3>技术性突破</h3>
