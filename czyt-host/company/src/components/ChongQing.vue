      <h3>6月21日</h3>
      <p>北京财资易通信息技术有限公司、重庆银行等单位共同签署了合作备忘录，将亚洲首家“企业级全球资金管理金融云平台”落户重庆。“金融云”项目是在重庆市政府及市金融办的大力支持下，由重庆银行作为国内首家主办行，北京财资易通信息技术有限公司作为方案提供方，共同发起设立。同时，该系统是基于环球同业银行金融电讯协会（SWIFT）现有产品和网络在全球应用的重大创新，也是与SWIFT合作的亚洲第一个“金融云”平台，届时平台上的企业用户可以直接使用财资管理系统对其全球资金进行管理。中国银行业协会专职副会长杨再平和SWIFT中国区总裁黄美伦分别向会议书面致贺。</p>
      <h3>银行业改革开放和创新发展的历程</h3>
      <p>纵观二十多年中国银行业改革开放和创新发展的历程，在获得举世瞩目成就和进步的同时，在为企业提供“跨境跨行金融支付结算”服务方面尚存在交易银行业务的盲区，是长期以来的“灯下黑”。同时，在国际结算、投融资以及人民币清算、外汇监管等诸多方面，以“跨境资金池”为代表的全球资金管理已悄然成为未来趋势。2016年由中国银行业协会倡导发起的“亚洲金融合作协会”就着重指出搭建金融合作平台、推进金融基础设施建设与加强支付结算和现金管理等业务的重要性。伴随全球经济一体化进程不断升级，以及关键技术及商业模式的进步，交易银行业务（即：以交易支付和现金管理为核心，针对企业供应链提供的金融服务）相对于传统信贷银行业务，在国际范围内有着更加迅速而深入的创新与应用，企业在全球资金管理方面期待更便捷、更安全可靠的金融环境和解决方案。</p>
      <h3>银企直连</h3>
